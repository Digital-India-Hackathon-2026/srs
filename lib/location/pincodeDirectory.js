/**
 * Look up a PIN code from the directory
 * @param {string} pin - 6-digit PIN code
 * @returns {object|null} - { locality, mandal, district, lat, lng } or null
 */
export function lookupPin(pin) {
  const entry = PIN_DIRECTORY[pin];
  if (!entry) return null;
  return {
    locality: entry.locality,
    mandal: entry.mandal,
    district: entry.district,
    lat: entry.lat,
    lng: entry.lng,
  };
}

/**
 * Get all PINs for a given district
 * @param {string} district
 * @returns {string[]}
 */
export function getPinsForDistrict(district) {
  return Object.entries(PIN_DIRECTORY)
    .filter(([, v]) => v.district === district)
    .map(([pin]) => pin);
}

/**
 * Haversine distance (km) between two lat/lng points
 */
export function haversine(lat1, lng1, lat2, lng2) {
  const R = 6371;
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}
